//while incremento, do 1 até 10 
let i = 1;
while(i <= 10) {
    console.log(i);
    i++;
}

//while decremento, do 10 até 1
let j = 10;
while (j >= 1) {
    console.log(j);
    j--;
}

//do while incremento, do 1 até 10
let k = 1;
do {
    console.log(k);
    k++;
} while (k <= 10);

//do while decremento, do 10 até 1
let l = 10;
do {
    console.log(l);
    l--;
} while (l >= 1);

//array com while
const nomes = ['João', 'Maria', 'José'];
let m = 0;
while (m < nomes.length) {
    console.log(nomes[m]);
    m++;
}

