//Igualdade de valor
console.log(1 == 1);

//Igualdade de tipo
console.log(1 === '1');

//Diferença de valor
console.log(1 != 1);

//Diferença de tipo
console.log(1 !== '1');

//Maior que
console.log(1 > 1);

//Menor que
console.log(1 < 1);

//Maior ou igual
console.log(1 >= 1);

//Menor ou igual
console.log(1 <= 1);



